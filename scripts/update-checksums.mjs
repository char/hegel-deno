// Regenerates the baked-in libhegel checksum table (src/checksums.ts).
//
// The downloader verifies each fetched libhegel artifact against a SHA-256
// digest compiled into the source (see LIBHEGEL_CHECKSUMS). Fetching a checksum
// file from the same release it verifies would be pointless — a tampered
// release would ship a matching checksum — so the digests live in the
// repository instead.
//
// This program queries a hegel-rust GitHub release (the latest by default, or
// the one named by the version argument) and reads the SHA-256 digest GitHub
// records for each client-requestable artifact (one per supported platform/arch,
// i.e. the .so/.dylib/.dll assets), then writes a fresh, generated source file
// pinning both the release version and the per-asset checksums. Run it to bump
// the pinned release, then commit the result.
//
// Usage:
//   node scripts/update-checksums.mjs [<version>]
//
// An empty version selects the latest release; otherwise the release tagged
// v<version> is pinned.

import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const REPO = "hegeldev/hegel-rust";
const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT = path.join(ROOT, "src", "checksums.ts");

// Matches the assets a client can actually request — one .so/.dylib/.dll per
// supported platform/arch. The .sha256 sidecar assets are irrelevant to runtime
// verification (GitHub records a digest for every asset) and are excluded.
const CLIENT_ASSET_RE = /^libhegel-[a-z0-9]+-[a-z0-9]+\.(so|dylib|dll)$/;

/**
 * Query a hegel-rust release and return its version (tag without a leading "v")
 * plus the SHA-256 digest GitHub records for each client-requestable asset.
 * Trusting GitHub's recorded digest is sound: it is computed server-side from
 * the stored bytes, so it matches what the downloader fetches over the same
 * channel.
 */
function release(want) {
  const args = ["release", "view"];
  if (want) {
    args.push(`v${want.replace(/^v/, "")}`);
  }
  args.push("--repo", REPO, "--json", "tagName,assets");

  const out = execFileSync("gh", args, { encoding: "utf8" });
  const rel = JSON.parse(out);

  const checksums = {};
  for (const asset of rel.assets) {
    if (!CLIENT_ASSET_RE.test(asset.name)) continue;
    const digest = asset.digest ?? "";
    if (!digest.startsWith("sha256:")) {
      throw new Error(
        `asset ${asset.name}: expected a sha256: digest, got ${JSON.stringify(digest)}`,
      );
    }
    checksums[asset.name] = digest.slice("sha256:".length);
  }

  const version = rel.tagName.replace(/^v/, "");
  if (want && version !== want.replace(/^v/, "")) {
    throw new Error(`requested release v${want.replace(/^v/, "")} but got tag ${rel.tagName}`);
  }
  return { version, checksums };
}

function render(version, checksums) {
  const entries = Object.keys(checksums)
    .sort()
    .map((name) => `  ${JSON.stringify(name)}: ${JSON.stringify(checksums[name])},`)
    .join("\n");

  return `// Code generated by scripts/update-checksums.mjs; DO NOT EDIT.
//
// Run \`just update-checksums\` to pull in the latest hegel-rust release, then
// commit the result.

/**
 * SHA-256 checksums for the published libhegel release artifacts.
 *
 * The auto-downloader (\`locate.ts\`) verifies a downloaded artifact against the
 * entry for the host platform's asset before loading it. The digests are baked
 * into the source on purpose: fetching a checksum file from the same release it
 * is meant to verify provides no integrity guarantee, since a tampered release
 * would simply ship a matching checksum.
 *
 * @packageDocumentation
 */

/** The pinned libhegel version this client targets. */
export const LIBHEGEL_VERSION = ${JSON.stringify(version)};

/** Map from published asset filename to its SHA-256 hex digest. */
export const LIBHEGEL_CHECKSUMS: Readonly<Record<string, string>> = {
${entries}
};
`;
}

function main() {
  const want = process.argv[2] ?? "";
  const { version, checksums } = release(want);
  process.stderr.write(`libhegel release: v${version}\n`);
  const names = Object.keys(checksums);
  if (names.length === 0) {
    throw new Error(`no .so/.dylib/.dll assets found in release v${version}`);
  }
  writeFileSync(OUTPUT, render(version, checksums));
  process.stderr.write(`wrote ${names.length} checksum(s) to ${path.relative(ROOT, OUTPUT)}\n`);
}

main();
